package com.Danthop.bionet.model;

public class CategoriaModel {
    private String id;
    private String name;

    public CategoriaModel(String id, String name) {
        this.id = id;
        this.name = name;

    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}

